class Result:
		def __init__(self):
			self._message = "No result found."

		def result(self):
			return _message

		def match(self):
			self._message = "Match"

		def noMatch(self):
			self._message = "No Match"

		def multipleFaces(self):
			self._message = "Multple Faces"

		def noFace(self):
			self._message = "No Face"
